import type { PackageManifestContribution } from './modifier/package-manifest-contributions'
import type { ComposeDSL } from './services/planner'
import type { TemplatePath } from '@/brand/template-path'
import type {
  GenerationPackageSpec,
  InternalDependencyLink,
} from '@/schema/generation-package-spec'
import type {
  CliProjectConfig,
  LibraryProjectConfig,
  NodeProjectConfig,
  ProjectConfig,
  ReactProjectConfig,
  VueProjectConfig,
  WorkspaceRootConfig,
} from '@/schema/project-config'
import { makeProjectName } from '@/brand/project-name'
import {
  buildPackageManifestJson,
  getPackageManifestContributions,
} from '@/core/modifier/package-json'
import { packageManifestTargetPath } from '@/core/modifier/package-manifest-contributions'
import {
  CliPackageOwner,
  contributionTrace,
  ContributionUnitKind,
  FrontendPackageOwner,
  LibraryPackageOwner,
  NodePackageOwner,
} from '@/core/ownership/model'
